package com.enation.cms.plugin.params;

/**
 * 自定义参数
 * @author kingapex
 *
 */
public class Param {
	private String name;
	private String value;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
